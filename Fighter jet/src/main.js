/**
 * ============================================================================
 * 游戏入口文件 (main.js)
 * ============================================================================
 *
 * 功能说明：
 * - 游戏的主入口点
 * - 配置 Phaser 3 游戏引擎
 * - 定义游戏画布大小、物理引擎、场景等
 * ============================================================================
 */

// 导入游戏场景
import { Menu } from './scenes/Menu.js';   // 菜单场景
import { Start } from './scenes/Start.js'; // 游戏主场景

/**
 * Phaser 游戏配置对象
 * 详细说明每个配置项的作用
 */
const config = {
    // ========== 渲染器配置 ==========
    type: Phaser.AUTO,              // 自动选择渲染器（优先WebGL，不支持则用Canvas）

    // ========== 游戏基本信息 ==========
    title: '航空战机',              // 游戏标题
    description: '击败敌机和BOSS，获得高分',  // 游戏描述

    // ========== 画布配置 ==========
    parent: 'game-container',       // 游戏画布挂载的HTML元素ID
    width: 1280,                    // 游戏画布宽度（像素）
    height: 720,                    // 游戏画布高度（像素）
    backgroundColor: '#1a1a2e',     // 背景颜色（深蓝紫色，科幻风格）
    pixelArt: false,                // 关闭像素艺术模式（允许图像抗锯齿）

    // ========== 场景配置 ==========
    scene: [
        Menu,                       // 第一个场景：主菜单（游戏启动时显示）
        Start                       // 第二个场景：游戏主场景
    ],

    // ========== 缩放配置 ==========
    scale: {
        mode: Phaser.Scale.FIT,              // 缩放模式：保持比例适应窗口
        autoCenter: Phaser.Scale.CENTER_BOTH // 自动居中：水平和垂直都居中
    },

    // ========== 物理引擎配置 ==========
    physics: {
        default: 'arcade',          // 使用 Arcade 物理引擎（适合2D射击游戏）
        arcade: {
            gravity: { y: 0 },      // 重力设置：Y轴为0（太空中无重力）
            debug: false            // 关闭物理调试模式（不显示碰撞框）
        }
    }
};

/**
 * 创建并启动 Phaser 游戏实例
 * 这行代码会：
 * 1. 根据配置创建游戏画布
 * 2. 初始化物理引擎
 * 3. 加载并启动第一个场景（Menu）
 */
new Phaser.Game(config);
